import Foundation

enum DateFormat: String {
    case HHmm = "HH:mm"
    case hhmm = "hh:mm"
    case ddMMyyyy = "dd/MM/yyyy"
    case yyyyMMddHH = "yyyy-MM-dd:HH"
    case HH = "HH"
    case EEE = "EEE"
    case yyyyMMdd = "yyyy-MM-dd"
    case EEEddMMyyyy = "EEE, dd/MM/yyyy"
}
